GameObject::GameObject(std::string name, Material* pMaterial) : Transform(name)
{
	SetupGL();
	material = pMaterial != nullptr ? *pMaterial : Material(glm::vec3(1, 1, 1));
}

unsigned GameObject::GetVAO() const
{
	return VAO;
}

Material GameObject::GetMaterial() const
{
	return material;
}


void GameObject::SetupGL()
{
	//1. Create VAO
	glGenVertexArrays(1, &VAO);
	glBindVertexArray(VAO);

	//2. Set Vertex Buffer Object
	glGenBuffers(1, &VBO);
	glBindBuffer(GL_ARRAY_BUFFER, VBO);
	glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

	//3. Set Attribute Pointers
	//Vertices
	//Refers to layout 0
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), reinterpret_cast<void*>(0));
	glEnableVertexAttribArray(0);
	
	//Normals
	glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), reinterpret_cast<void*>(3 * sizeof(float)));
	glEnableVertexAttribArray(1);

	//UVs
    glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), reinterpret_cast<void*>(6 * sizeof(float)));
    glEnableVertexAttribArray(2);

	//EBO HERE!
		//unsigned int EBO;
		//glGenBuffers(1, &EBO);
		//glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, EBO);
		//glBufferData(GL_ELEMENT_ARRAY_BUFFER, sizeof(indices), indices, GL_STATIC_DRAW);

	glBindVertexArray(0);
	glBindBuffer(GL_ARRAY_BUFFER, 0);
}
